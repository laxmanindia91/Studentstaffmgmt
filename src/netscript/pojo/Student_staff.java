package netscript.pojo;

public class Student_staff {
	
	int staff_id,student_id;
	String stud_name,stud_city;
	
	//getter and setter for both table-column
	public int getStaff_id() {
		return staff_id;
	}
	public void setStaff_id(int staff_id) {
		this.staff_id = staff_id;
	}
	public int getStudent_id() {
		return student_id;
	}
	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}
	public String getStud_name() {
		return stud_name;
	}
	public void setStud_name(String stud_name) {
		this.stud_name = stud_name;
	}
	public String getStud_city() {
		return stud_city;
	}
	public void setStud_city(String stud_city) {
		this.stud_city = stud_city;
	}

}
